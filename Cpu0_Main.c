void core0_main(void)
{
    Intialization();
    while(1)
    {
        ControlUnit();  // 모드 변환 함수 호출(SW1: ON<->OFF, SW2: ON<->CA)
        dist = DetectDist();  // 거리 측정 함수 호출(초음파센서활용)

        // 거리에 따라 플래그 설정
        if(dist <= 20)
        {
            flag = 1;  // 거리가 20 이하이면 flag를 1로 설정
        }
        else if(dist > 20 && dist <= 50)
        {
            flag = 2;  // 거리가 20 초과 50 이하이면 flag를 2로 설정
        }
        else
        {
            flag = 0;  // 거리가 50 초과이면 flag를 0으로 설정
        }

        // 애플리케이션 스케줄링 함수 호출
        AppScheduling(state, flag);
    }
}
void ControlUnit() {
    int currentStateSW1 = IfxPort_getPinState(SW1.port, SW1.pinIndex);  // SW1의 현재 상태를 읽어와 변수에 저장
    int currentStateSW2 = IfxPort_getPinState(SW2.port, SW2.pinIndex);  // SW2의 현재 상태를 읽어와 변수에 저장

    //SW1: ON<->OFF
    if (currentStateSW1 != lastStateSW1)  // SW1의 상태가 변경되었는지 확인
    {
        lastStateSW1 = currentStateSW1;  // SW1의 이전 상태를 현재 상태로 업데이트
        if (currentStateSW1)  // SW1이 눌러진 상태인지 확인
        {
            if (state == MODE_OFF)  // 현재 시스템 상태가 MODE_OFF인지 확인
            {
                state = MODE_ON;  // MODE_OFF일 때 SW1이 눌리면 시스템을 MODE_ON으로 변경
            }
            else
            {
                state = MODE_OFF;  // MODE_ON일 때 SW1이 눌리면 시스템을 MODE_OFF로 변경
            }
        }
    }
    //SW2: ON<->CA
    if (currentStateSW2 != lastStateSW2)  // SW2의 상태가 변경되었는지 확인
    {
        lastStateSW2 = currentStateSW2;  // SW2의 이전 상태를 현재 상태로 업데이트
        if (currentStateSW2)  // SW2가 눌러진 상태인지 확인
        {
            if (state == MODE_ON)  // 현재 시스템 상태가 MODE_ON인지 확인
            {
                state = MODE_CA;  // MODE_ON일 때 SW2가 눌리면 시스템을 MODE_CA(충돌회피모드)로 변경
            }
            else if (state == MODE_CA)  // 현재 시스템 상태가 MODE_CA(충돌회피모드)인지 확인
            {
                state = MODE_ON;  // MODE_CA(충돌회피모드)일 때 SW2가 눌리면 시스템을 MODE_ON으로 변경
            }
        }
    }
}
void Intialization(void){
    // 포트 초기화 함수 호출
    Driver_Port_Init();

    // 모터(PWM) 초기화 함수 호출
    initGtmTomPwm();

    // CPU 인터럽트 활성화
    IfxCpu_enableInterrupts();

    //워치독 비활성화
    IfxScuWdt_disableCpuWatchdog(IfxScuWdt_getCpuWatchdogPassword());
    IfxScuWdt_disableSafetyWatchdog(IfxScuWdt_getSafetyWatchdogPassword());

    /* CPU 동기화 이벤트 대기 */
    IfxCpu_emitEvent(&g_cpuSyncEvent);
    IfxCpu_waitEvent(&g_cpuSyncEvent, 1);

    // 타이머 초기화 함수 호출
    Driver_Stm_Init();

    // 초음파 센서 초기화 함수 호출
    initUSonic();
    initCCU60();
    initCCU61();
    initERU();

    //차량초기목표속도 설정
    Drive_init();

    //엔코더 초기화
    SetCountZero();
}
