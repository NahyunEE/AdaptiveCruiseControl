double m_LPF(double old_w, double new_w, int cutoff_freq){
    float32 Ts = 0.001;  // 샘플링 주기 0.001로 초기화
    float32 tau = 0.0;    // 필터 시간 상수 0.0으로 초기화
    float32 filtered_w;   // 필터링된 값을 저장할 변수 선언

    tau = Ts * cutoff_freq;  // 필터 시간 상수를 샘플링 주기와 Cut-off Freq로 계산

    // 저역 통과 필터링을 수행하여 새로운 값에 대한 필터링된 값을 계산하여 변수에 저장
    filtered_w = ((1 - tau) * old_w) + (tau * new_w);

    return filtered_w;  // 필터링된 값 반환
}

