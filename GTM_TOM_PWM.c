/*------------------------------------------------------Macros-------------------------------------------------------*/
/*********************************************************************************************************************/
#define ISR_PRIORITY_TOM    20                                      /* Interrupt priority number                    */
#define MOTOR1              IfxGtm_TOM0_3_TOUT105_P10_3_OUT
#define MOTOR2              IfxGtm_TOM0_9_TOUT1_P02_1_OUT       /* LED which will be driven by the PWM          */
#define PWM_PERIOD          50000                                   /* PWM period for the TOM                       */
#define FADE_STEP           PWM_PERIOD / 100                        /* PWM duty cycle for the TOM                   */

/*********************************************************************************************************************/
/*-------------------------------------------------Global variables--------------------------------------------------*/
/*********************************************************************************************************************/
IfxGtm_Tom_Pwm_Config g_tomConfig1;                                  /* Timer configuration structure                */
IfxGtm_Tom_Pwm_Driver g_tomDriver1;
IfxGtm_Tom_Pwm_Config g_tomConfig2;                                  /* Timer configuration structure                */
IfxGtm_Tom_Pwm_Driver g_tomDriver2;/* Timer Driver structure                       */
uint32 g_fadeValue = 0;                                             /* Fade value, starting from 0                  */
sint8 g_fadeDir = 1;                                                /* Fade direction variable                      */

/*********************************************************************************************************************/
/*-----------------------------------------------Function Prototypes-------------------------------------------------*/
/*********************************************************************************************************************/

