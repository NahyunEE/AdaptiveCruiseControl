/* This function initializes the TOM */
void initGtmTomPwm(void)
{
    IfxGtm_enable(&MODULE_GTM);                                     /* Enable GTM                                   */

    IfxGtm_Cmu_enableClocks(&MODULE_GTM, IFXGTM_CMU_CLKEN_FXCLK);   /* Enable the FXU clock                         */

    /* Initialize the configuration structure with default parameters */
    IfxGtm_Tom_Pwm_initConfig(&g_tomConfig1, &MODULE_GTM);

    g_tomConfig1.tom = MOTOR1.tom;                                      /* Select the TOM depending on the LED          */
    g_tomConfig1.tomChannel = MOTOR1.channel;                           /* Select the channel depending on the LED      */
    g_tomConfig1.period = PWM_PERIOD;                                /* Set the timer period                         */
    g_tomConfig1.pin.outputPin = &MOTOR1;                               /* Set the LED port pin as output               */
    g_tomConfig1.dutyCycle = 0;
    g_tomConfig1.synchronousUpdateEnabled = TRUE;                    /* Enable synchronous update                    */


    IfxGtm_Tom_Pwm_init(&g_tomDriver1, &g_tomConfig1);                /* Initialize the GTM TOM                       */
    IfxGtm_Tom_Pwm_start(&g_tomDriver1, TRUE);                       /* Start the PWM                                */


    IfxGtm_Tom_Pwm_initConfig(&g_tomConfig2, &MODULE_GTM);
    g_tomConfig2.tom = MOTOR2.tom;                                      /* Select the TOM depending on the LED          */
    g_tomConfig2.tomChannel = MOTOR2.channel;                           /* Select the channel depending on the LED      */
    g_tomConfig2.period = PWM_PERIOD;                                /* Set the timer period                         */
    g_tomConfig2.pin.outputPin = &MOTOR2;                               /* Set the LED port pin as output               */
    g_tomConfig2.dutyCycle =0;
    g_tomConfig2.synchronousUpdateEnabled = TRUE;                    /* Enable synchronous update                    */

    IfxGtm_Tom_Pwm_init(&g_tomDriver2, &g_tomConfig2);                /* Initialize the GTM TOM                       */
    IfxGtm_Tom_Pwm_start(&g_tomDriver2, TRUE);


}

/* This function sets the duty cycle of the PWM */
void setDutyCycleA(int dutyCycle)
{
    g_tomConfig1.dutyCycle = dutyCycle; // dutyCycle 값을 PWM 구성 설정에 할당 (A 채널)
    IfxGtm_Tom_Pwm_init(&g_tomDriver1, &g_tomConfig1); // PWM을 재초기화
}

void setDutyCycleB(int dutyCycle)
{
    g_tomConfig2.dutyCycle = dutyCycle; // dutyCycle 값을 PWM 구성 설정에 할당 (B 채널)
    IfxGtm_Tom_Pwm_init(&g_tomDriver2, &g_tomConfig2); // PWM을 재초기화
}
